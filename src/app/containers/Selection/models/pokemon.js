class Pokemon {
  constructor(name, type1, type2, weakness, height, width, attack, defense, speed, special) {
    this.name = name;
    this.type1 = type1;
    this.type2 = type2;
    this.weakness = weakness;
    this.height = height;
    this.width = width;
    this.attack = attack;
    this.defense = defense;
    this.speed = speed;
    this.special = special;
  }
}

export default Pokemon;
